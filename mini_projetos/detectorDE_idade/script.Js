function verificar() {
  var data = new Date();
  var ano = data.getFullYear();
  var fano = document.getElementById("txtano");
  var res = document.getElementById("res");
  if (fano.value.length == 0 || fano.value > ano) {
    window.alert("[ERRO] Verifique os dados e tente novamente!");
  } else {
    var fsex = document.getElementsByName("radsex");
    var idade = ano - Number(fano.value);
    var genero = "";
    var img = document.createElement("img");
    img.setAttribute("id", "foto");
    if (fsex[0].checked) {
      genero = "Homem";
      if (idade >= 0 && idade < 10) {
        //crianÃ§a
        img.setAttribute("src", "./img/crianÃ§aM.png");
      } else if (idade < 21) {
        //jovem
        img.setAttribute("src", "./img/jovemM.png");
      } else if (idade < 50) {
        //adulto
        img.setAttribute("src", "./img/adultoM.png");
      } else {
        //idoso
        img.setAttribute("src", "./img/velhoM.png");
      }
    } else if (fsex[1].checked) {
      genero = "Mulher";
      if (idade >= 0 && idade < 10) {
        //crianÃ§a
        img.setAttribute("src", "./img/crianÃ§aF.png");
      } else if (idade < 21) {
        //jovem
        img.setAttribute("src", "./img/jovemF.png");
      } else if (idade < 50) {
        //adulto
        img.setAttribute("src", "./img/adultoF.png");
      } else {
        //idoso
        img.setAttribute("src", "./img/velhoF.png");
      }
    }
    res.style.textAlign = "center";
    res.innerHTML = `Detectamos ${genero} com ${idade} anos.`;
    res.appendChild(img);
  }
}
